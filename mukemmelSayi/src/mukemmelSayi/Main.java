package mukemmelSayi;

public class Main {

	public static void main(String[] args) {
// kendisinden baþka bölündüðü sayýlarýn toplamý kendisini veriyorsa bu sayý mükemmel sayýdýr.

// örneðin 6 -> 1, 2, 3 -> 1+2+3=6 & 28 -> 1, 2, 4, 7, 14 -> 1+2+4+7+14=28

		int number = 28;
		int total = 0;

		for (int i = 1; i < number; i++) {
			if (number % i == 0) {
				total = total + i;
			}
		}
		if (total == number) {
			System.out.println("Mükemmel sayýdýr");
		} else {
			System.out.println("Mükemmel sayý deðildir");
		}

	}
}
